pub type BitcoinAddresses = BTreeMap<String, String>;

#[derive(CandidType, Clone, Debug, Deserialize, Serialize)]
pub struct Config {
    pub fee_address: BitcoinAddresses,

    pub fee_per_input: u64,
    pub fee_basis_points: u64,
    pub fixed_cost: u64,
}

#[worker::send]
pub async fn config(
    State(app_state): State<AppState>,
) -> Result<Json<Config>, (StatusCode, String)> {
    let agent = app_state.agent.clone();
    let response = agent
        .query(&SCROLLS_BITCOIN, "config")
        .call()
        .await
        .map_err(|e| {
            (
                StatusCode::BAD_GATEWAY,
                format!("System error: querying config: {}", e),
            )
        })?;
    let config = Decode!(&response, Config).map_err(|e| {
        (
            StatusCode::BAD_GATEWAY,
            format!("System error: decoding config: {}", e),
        )
    })?;

    Ok(Json(config))
}

#[worker::send]
pub async fn address(
    State(app_state): State<AppState>,
    Path((network, nonce)): Path<(String, u64)>,
) -> Result<Json<String>, (StatusCode, String)> {
    let agent = app_state.agent.clone();
    let update_response = agent
        .update(&SCROLLS_BITCOIN, "address")
        .with_arg(Encode!(&network, &nonce).map_err(|e| {
            (
                StatusCode::INTERNAL_SERVER_ERROR,
                format!("System error: encoding address request: {}", e),
            )
        })?)
        .call()
        .await
        .map_err(|e| {
            (
                StatusCode::BAD_GATEWAY,
                format!("System error: calling address method: {}", e),
            )
        })?;
    let response = wait_for_response(agent, update_response)
        .await
        .map_err(|e| {
            (
                StatusCode::BAD_GATEWAY,
                format!("System error: waiting for response: {}", e),
            )
        })?;
    let result = Decode!(&response, Result<String, String>)
        .map_err(|e| {
            (
                StatusCode::INTERNAL_SERVER_ERROR,
                format!("System error: decoding address response: {}", e),
            )
        })?
        .map_err(|e| (StatusCode::BAD_GATEWAY, format!("Upstream error: {}", e)))?;
    Ok(Json(result))
}

async fn wait_for_response(
    agent: Arc<Agent>,
    update_response: CallResponse<(Vec<u8>, Certificate)>,
) -> anyhow::Result<Vec<u8>> {
    match update_response {
        CallResponse::Response((response, ..)) => Ok(response),
        CallResponse::Poll(request_id) => loop {
            let (request_status, _) = agent
                .request_status_raw(&request_id, *SCROLLS_BITCOIN)
                .await?;
            match request_status {
                RequestStatusResponse::Unknown => {}
                RequestStatusResponse::Received => {}
                RequestStatusResponse::Processing => {}
                RequestStatusResponse::Replied(ReplyResponse { arg }) => {
                    break Ok(arg);
                }
                RequestStatusResponse::Rejected(RejectResponse {
                    reject_code,
                    reject_message,
                    ..
                }) => {
                    break Err(anyhow!(
                        "Request rejected: code {reject_code:?}: {reject_message}"
                    ))
                }
                RequestStatusResponse::Done => {
                    break Err(anyhow!("Request is done but no reply received"))
                }
            };
            TimeoutFuture::new(1000).await;
        },
    }
}

#[derive(CandidType, Clone, Debug, Deserialize, Serialize)]
pub struct SignInput {
    pub index: usize,
    pub nonce: u64,
}

#[derive(CandidType, Clone, Debug, Deserialize, Serialize)]
pub struct SignRequest {
    pub sign_inputs: Vec<SignInput>,
    pub prev_txs: Vec<String>,
    pub tx_to_sign: String,
}

#[worker::send]
pub async fn sign(
    State(app_state): State<AppState>,
    Path(network): Path<String>,
    Json(sign_request): Json<SignRequest>,
) -> Result<Json<String>, (StatusCode, String)> {
    let agent = app_state.agent.clone();
    let update_response = agent
        .update(&SCROLLS_BITCOIN, "sign")
        .with_arg(Encode!(&network, &sign_request).map_err(|e| {
            (
                StatusCode::INTERNAL_SERVER_ERROR,
                format!("System error: encoding sign request: {}", e),
            )
        })?)
        .call()
        .await
        .map_err(|e| {
            (
                StatusCode::BAD_GATEWAY,
                format!("System error: calling sign method: {}", e),
            )
        })?;
    let response = wait_for_response(agent, update_response)
        .await
        .map_err(|e| {
            (
                StatusCode::BAD_GATEWAY,
                format!("System error: waiting for response: {}", e),
            )
        })?;
    let result = Decode!(&response, Result<String, String>)
        .map_err(|e| {
            (
                StatusCode::INTERNAL_SERVER_ERROR,
                format!("System error: decoding sign response: {}", e),
            )
        })?
        .map_err(|e| (StatusCode::BAD_GATEWAY, format!("Upstream error: {}", e)))?;
    Ok(Json(result))
}

